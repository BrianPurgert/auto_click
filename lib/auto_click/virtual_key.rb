	leftwin:     0x5B, rightwin: 0x5C, app: 0x5D, application: 0x5D,
	# and so on...

	}.freeze

	def self.code_from_name(name)
		return name if name.is_a? Integer

		if name.is_a? String
			name = name.downcase.delete('_- ').to_sym
		end

		KEYNAMES_TO_CODES.fetch(name, 0)
	end

end
