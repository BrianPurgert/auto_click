	cap:         0x14, capslock: 0x14, caplock: 0x14, esc: 0x1B,
	escape:      0x1B, space: 0x20, spacebar: 0x20, pageup: 0x21,
	pagedown:    0x22, end: 0x23, home: 0x24, left: 0x25,
	leftarrow:   0x25, leftkey: 0x25, up: 0x26, uparrow: 0x26,
	upkey:       0x26, right: 0x27, rightarrow: 0x27, rightkey: 0x27,
	down:        0x28, downarrow: 0x28, downkey: 0x28, select: 0x29,
