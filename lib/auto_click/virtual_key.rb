	printscreen: 0x2C, ins: 0x2D, insert: 0x2D, del: 0x2E,
	delete:      0x2E, help: 0x2F, num0: 0x30, number0: 0x30,
	num1:        0x31, number1: 0x31, num2: 0x32, number2: 0x32,
